export const PASSWORD_WORD_CANDIDATES = PASSWORD_WORD_VALUES.map(
	(value) => ({ length: value.length, value }) satisfies WordCandidate,
);

export function getTypeableWordCandidates(pool: string): WordCandidate[] {
	const poolSet = new Set(pool);

	return PASSWORD_WORD_CANDIDATES.filter(({ value }) =>
		Array.from(value).every((character) => poolSet.has(character)),
	);
}

export function groupWordCandidatesByLength(
	candidates: WordCandidate[],
): Map<number, WordCandidate[]> {
	const groups = new Map<number, WordCandidate[]>();

	for (const candidate of candidates) {
		const existingGroup = groups.get(candidate.length) ?? [];
		existingGroup.push(candidate);
		groups.set(candidate.length, existingGroup);
	}

	return groups;
}

export function countCompleteWordSequences(
	targetLength: number,
	candidatesByLength: Map<number, WordCandidate[]>,
): bigint[] {
	const sequenceCounts = Array.from({ length: targetLength + 1 }, () => 0n);
	sequenceCounts[0] = 1n;

	for (let length = 1; length <= targetLength; length += 1) {
		let count = 0n;

		for (const [wordLength, candidates] of candidatesByLength) {
			if (wordLength > length) {
				continue;
			}

			count +=
				BigInt(candidates.length) * (sequenceCounts[length - wordLength] ?? 0n);
		}

		sequenceCounts[length] = count;
	}

	return sequenceCounts;
}
